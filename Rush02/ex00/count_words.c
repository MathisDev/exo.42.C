int	count_words(char *str)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (str[i])
	{
		if ((('A' <= str[i] && str[i] <= 'Z')
				|| ('a' <= str[i] && str[i] <= 'z')
				|| ('0' <= str[i] && str[i] <= '9')) && (str[i - 1] == ' '
				|| str[i - 1] == ':' || str[i - 1] == '\n' || i == 0))
			count++;
		i++;
	}
	return (count);
}
