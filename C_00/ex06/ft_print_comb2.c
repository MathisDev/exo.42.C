#include <unistd.h>

void	print(char a, char b, char c, char d)
{
	write(1, &a, 1);
	write(1, &b, 1);
	write(1, " ", 1);
	write(1, &c, 1);
	write(1, &d, 1);
	if (a != '9' || b != '8' || c != '9' || d != '9')
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	b = 0;
	while (a != 98)
	{
		b ++;
		if (b == 100)
		{
			a ++;
			b = a + 1;
		}
		print(a / 10 + '0', a % 10 + '0', b / 10 + '0', b % 10 + '0');
	}
}
